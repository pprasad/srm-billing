    private void clean(){
       customerNameTxt.setText("");
       mobileNoTxt.setText("");
       emailIdTxt.setText("");
       seleObj=null;
    }
    private void setModel(){
        customerTableModel=new CustomerTableModel();
        customerDetailsTable.setModel(customerTableModel);
    }
    public void loadData(){
        customerTableModel.removeAll();
        List<Customer> customers=customerService.findAllCustomers();
        customerTableModel.addRows(customers);
        clean();
    }

